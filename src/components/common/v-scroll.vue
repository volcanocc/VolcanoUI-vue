<template>
    <div ref="wrapper" class="v-scroll-wrapper">
        <div class="v-scroll-content">
            <div ref="listWrapper">
                <slot>
                    <ul class="v-scroll-list">
                        <li @click="clickItem(item)" class="v-scroll-item border-bottom-1px" v-for="item in data">
                            {{item}}
                        </li>
                    </ul>
                </slot>
            </div>
            <slot name="pullup" :pullUpLoad="pullUpLoad" :isPullUpLoad="isPullUpLoad">
                <div class="v-pullup-wrapper" v-if="pullUpLoad">
                    <div class="before-trigger" v-if="!isPullUpLoad">
                        <span>{{ pullUpTxt }}</span>
                    </div>
                    <div class="after-trigger" v-else>
                        <v-loading></v-loading>
                    </div>
                </div>
            </slot>
        </div>
        <slot
            name="pulldown"
            :pullDownRefresh="pullDownRefresh"
            :pullDownStyle="pullDownStyle"
            :beforePullDown="beforePullDown"
            :isPullingDown="isPullingDown"
            :bubbleY="bubbleY">
            <div class="v-pulldown-wrapper" :style="pullDownStyle" v-if="pullDownRefresh">
                <div class="before-trigger" v-if="beforePullDown">
                    <v-bubble :y="bubbleY"></v-bubble>
                </div>
                <div class="after-trigger" v-else>
                    <div v-if="isPullingDown" class="loading">
                        <v-loading></v-loading>
                    </div>
                    <div v-else><span>{{ refreshTxt }}</span></div>
                </div>
            </div>
        </slot>
    </div>
</template>

<script type="text/ecmascript-6">
    import BScroll from 'better-scroll'
    import VLoading from './v-loading'
    import VBubble from './v-bubble.vue'
    import {getRect} from '../../assets/common/helpers/dom'

    const COMPONENT_NAME = 'v-scroll'
    const DIRECTION_H = 'horizontal'
    const DIRECTION_V = 'vertical'
    const DEFAULT_LOAD_TXT_MORE = 'Load more'
    const DEFAULT_LOAD_TXT_NO_MORE = 'No more data'
    const DEFAULT_REFRESH_TXT = 'Refresh success'

    const EVENT_SCROLL = 'scroll'
    const EVENT_BEFORE_SCROLL_START = 'before-scroll-start'
    const EVENT_CLICK = 'click'
    const EVENT_PULLING_DOWN = 'pulling-down'
    const EVENT_PULLING_UP = 'pulling-up'

    const DEFAULT_OPTIONS = {
        observeDOM: true,
        click: true,
        probeType: 1,
        scrollbar: false,
        pullDownRefresh: false,
        pullUpLoad: false
    }

    export default {
        name: COMPONENT_NAME,
        props: {
            data: {
                type: Array,
                default() {
                    return []
                }
            },
            options: {
                type: Object,
                default() {
                    return {}
                }
            },
            listenScroll: {
                type: Boolean,
                default: false
            },
            listenBeforeScroll: {
                type: Boolean,
                default: false
            },
            direction: {
                type: String,
                default: DIRECTION_V
            },
            refreshDelay: {
                type: Number,
                default: 20
            },
            listRef: {
                type: String,
                default: 'list'
            }
        },
        data() {
            return {
                beforePullDown: true,
                isRebounding: false,
                isPullingDown: false,
                isPullUpLoad: false,
                pullUpDirty: true,
                bubbleY: 0,
                pullDownStyle: ''
            }
        },
        computed: {
            pullUpLoad() {
                return this.options.pullUpLoad
            },
            pullDownRefresh() {
                return this.options.pullDownRefresh
            },
            pullUpTxt() {
                const pullUpLoad = this.pullUpLoad
                const txt = pullUpLoad && pullUpLoad.txt
                const moreTxt = txt && txt.more || DEFAULT_LOAD_TXT_MORE
                const noMoreTxt = txt && txt.noMore || DEFAULT_LOAD_TXT_NO_MORE

                return this.pullUpDirty ? moreTxt : noMoreTxt
            },
            refreshTxt() {
                const pullDownRefresh = this.pullDownRefresh
                return pullDownRefresh && pullDownRefresh.txt || DEFAULT_REFRESH_TXT
            }
        },
        created() {
            this.pullDownInitTop = -50
        },
        mounted() {
            this.$nextTick(() => {
                this.initScroll()
            })
        },
        methods: {
            initScroll() {
                if (!this.$refs.wrapper) {
                    return
                }
                if (this.$refs.listWrapper && (this.pullDownRefresh || this.pullUpLoad)) {
                    this.$refs.listWrapper.style.minHeight = `${getRect(this.$refs.wrapper).height + 1}px`
                }

                let options = Object.assign({}, DEFAULT_OPTIONS, {
                    scrollY: this.direction === DIRECTION_V,
                    scrollX: this.direction === DIRECTION_H
                }, this.options)

                this.scroll = new BScroll(this.$refs.wrapper, options)

                if (this.listenScroll) {
                    this.scroll.on('scroll', (pos) => {
                        this.$emit(EVENT_SCROLL, pos)
                    })
                }

                if (this.listenBeforeScroll) {
                    this.scroll.on('beforeScrollStart', () => {
                        this.$emit(EVENT_BEFORE_SCROLL_START)
                    })
                }

                if (this.pullDownRefresh) {
                    this._initPullDownRefresh()
                }

                if (this.pullUpLoad) {
                    this._initPullUpLoad()
                }
            },
            disable() {
                this.scroll && this.scroll.disable()
            },
            enable() {
                this.scroll && this.scroll.enable()
            },
            refresh() {
                this.scroll && this.scroll.refresh()
            },
            destroy() {
                this.scroll.destroy()
            },
            scrollTo() {
                this.scroll && this.scroll.scrollTo.apply(this.scroll, arguments)
            },
            scrollToElement() {
                this.scroll && this.scroll.scrollToElement.apply(this.scroll, arguments)
            },
            clickItem(item) {
                this.$emit(EVENT_CLICK, item)
            },
            forceUpdate(dirty) {
                if (this.pullDownRefresh && this.isPullingDown) {
                    this.isPullingDown = false
                    this._reboundPullDown().then(() => {
                        this._afterPullDown()
                    })
                } else if (this.pullUpLoad && this.isPullUpLoad) {
                    this.isPullUpLoad = false
                    this.scroll.finishPullUp()
                    this.pullUpDirty = dirty
                    this.refresh()
                } else {
                    this.refresh()
                }
            },
            _initPullDownRefresh() {
                this.scroll.on('pullingDown', () => {
                    this.beforePullDown = false
                    this.isPullingDown = true
                    this.$emit(EVENT_PULLING_DOWN)
                })

                this.scroll.on('scroll', (pos) => {
                    if (this.beforePullDown) {
                        this.bubbleY = Math.max(0, pos.y + this.pullDownInitTop)
                        this.pullDownStyle = `top:${Math.min(pos.y + this.pullDownInitTop, 10)}px`
                    } else {
                        this.bubbleY = 0
                    }

                    if (this.isRebounding) {
                        this.pullDownStyle = `top:${Math.min(pos.y - 30, 10)}px`
                    }
                })
            },
            _initPullUpLoad() {
                this.scroll.on('pullingUp', () => {
                    this.isPullUpLoad = true
                    this.$emit(EVENT_PULLING_UP)
                })
            },
            _reboundPullDown() {
                const {stopTime = 600} = this.pullDownRefresh
                return new Promise((resolve) => {
                    setTimeout(() => {
                        this.isRebounding = true
                        this.scroll.finishPullDown()
                        this.isPullingDown = false
                        resolve()
                    }, stopTime)
                })
            },
            _afterPullDown() {
                setTimeout(() => {
                    this.pullDownStyle = `top:${this.pullDownInitTop}px`
                    this.beforePullDown = true
                    this.isRebounding = false
                    this.refresh()
                }, this.scroll.options.bounceTime)
            }
        },
        watch: {
            data() {
                setTimeout(() => {
                    this.forceUpdate(true)
                }, this.refreshDelay)
            }
        },
        components: {
            VLoading,
            VBubble
        }
    }

</script>

